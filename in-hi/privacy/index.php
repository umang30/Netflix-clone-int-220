हम उन अनुरोधों को अस्वीकार कर सकते हैं जो अनुचित हैं या कानून द्वारा अपेक्षित नहीं हैं इनमें वे भी शामिल हैं जो अत्यंत अव्यावहारिक हैं, जिनके लिए असंगत तकनीकी प्रयासों की जरूरत हो सकती है, या हमारा संपर्क संचालनात्मक जोखिम से हो सकता है जैसे मुफ़्त ट्रायल फ़्रॉड. हम जानकारी को जितनी आवश्यकता हो या लागू कानूनों द्वारा जितनी अनुमति हो उतने समय रख सकते हैं, जिसमें आपके विकल्प का सम्मान करना, हमारे बिलिंग या रिकॉर्ड उद्देश्य के लिए और इस प्रायवेसी स्टेटमेंट में वर्णित उद्देश्यों को पूरा करने के लिए शामिल है. जब आवश्यकता नहीं रह जाती है, तो हम पर्सनल जानकारी की पहचान को सुरक्षित तरीके से नष्ट करने या अनाम करने के लिए उचित उपाय करते हैं.
