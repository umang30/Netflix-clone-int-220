आप अपनी पर्सनल जानकारी को एक्सेस करने या हमारे पास मौजूद अपनी आउट-ऑफ़-डेट या गलत पर्सनल जानकारी को सही करने या अपडेट करने के लिए अनुरोध कर सकते हैं. आप यह अनुरोध भी कर सकते हैं कि हम अपने पास से आपकी पर्सनल जानकारी हटा दें.
