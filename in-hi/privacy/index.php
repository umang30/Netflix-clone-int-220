<b>
आपकी चॉइसेस (विकल्प)
</b>
<br/>
<b>ईमेल और टेक्स्ट संदेश.</b> अगर आप अब हमसे ईमेल या टेक्स्ट संदेश द्वारा कुछ संचार प्राप्त नहीं करना चाहते हैं, तो हमारी वेबसाइट के “अकाउंट” सेक्शन में “कम्यूनिकेशन्स सेटिंग्स” विकल्प को एक्सेस करें और जिन आइटमों को अनसब्सक्राइब करना है उन्हें अनचेक करें. वैकल्पिक रूप से, ईमेल में "अनसब्सक्राइब" लिंक को क्लिक करें या टेक्स्ट संदेश का STOP जवाब दें (या जैसा अन्यथा निर्देशित हो). कृपया ध्यान दें कि आप हमसे सर्विस-संबंधी पत्राचार से अनसब्सक्राइब नहीं कर सकते, जैसे आपके अकाउंट ट्रांजेक्शन से संबंधित मेसेज.
<br />
<br />
<b>पुश नोटिफ़िकेशन.</b> आप Netflix से मोबाइल पुश नोटिफ़िकेशन प्राप्त करना चुन सकते हैं. कुछ डिवाइस ऑपरेटिंग सिस्टम पर, आप अपनेआप नोटिफ़िकेशन में नामांकित हो जाते हैं. अगर आप बाद में निर्णय करते हैं कि अब आप आगे ये नोटिफ़िकेशन प्राप्त नहीं करना चाहते हैं, आप उनको ऑफ़ करने के लिए अपने मोबाइल डिवाइस की सेटिंग फ़ंक्शनैलिटी का इस्तेमाल कर सकते हैं. हम कुछ वेब ब्राउज़रों पर भी पुश नोटिफ़िकेशन की पेशकश करते हैं. अगर आप उन नोटिफ़िकेशनों को प्राप्त करने के लिए सहमत होते हैं और बाद में निर्णय करते हैं कि अब आप आगे इन नोटिफ़िकेशनों को प्राप्त नहीं करना चाहते, तो आप उन्हें ऑफ़ करने के लिए अपने ब्राउज़र की सेटिंग का इस्तेमाल कर सकते हैं.
<br />
<br />
<b>WhatsApp.</b> अगर आपने अपने Netflix अकाउंट या प्रोफ़ाइल के संबंध में WhatsApp मेसेज को एनेबल किया है और अब आगे उन्हें प्राप्त नहीं करना चाहते हैं तो उन्हें ब्लॉक करने के लिए आप अपने WhatsApp की सेटिंग्स का इस्तेमाल कर सकते हैं.
<br />
<br />
<b>पसंद-आधारित ऐड्स.</b> पसंद-आधारित ऐड्स (विज्ञापन) ऑनलाइन ऐड्स हैं जिन्हें पूरे इंटरनेट पर विभिन्न ऐप्स और वेबसाइटों के इस्तेमाल के आधार पर आपकी संभावित पसंद के अनुसार अनुकूलित किया गया है. अगर आप किसी ब्राउज़र का इस्तेमाल कर रहे हैं, तो कुकीज़ और वेब बीकन का इस्तेमाल आपकी संभावित पसंद का लगाने में मदद करने के लिए जानकारी एकत्रित करने के लिए कर सकते हैं. अगर आप किसी मोबाइल डिवाइस, टैबलेट, या स्ट्रीमिंग मीडिया डिवाइस का इस्तेमाल कर रहे हैं जिसमें रीसेट होने वाला डिवाइस आइडेंटीफ़ायर शामिल है, तो उस आइडेंटीफ़ायर का इस्तेमाल आपकी संभावित पसंद निर्धारित करने में मदद के लिए किया जा सकता है. Netflix से पसंद-आधारित ऐड के बारे में आपके विकल्प के लिए, कृपया "कुकीज़ और इंटरनेट एडवर्टाइज़िंग" सेक्शन (नीचे) देखें.
<br />
<br />
<b>मैच किए गए आइडेंटीफ़ायर कम्यूनिकेशन्स.</b> कुछ थर्ड पार्टी (तृतीय पक्ष) साइट और ऐप के कारण हम अपने यूजर्स तक पहुंच सकते हैं ऑनलाइन प्रमोशन के साथ थर्ड पार्टी को एक प्रायवेसी संरक्षक आइडेंटीफ़ायर भेज कर हमारे टाइटल और सेवाओं के बारे में ऑनलाइन प्रमोशन वाले हमारे यूज़र्स तक पहुंच सकते हैं. एक प्रायवेसी संरक्षक आइडेंटीफ़ायर का मतलब है हम मूल जानकारी (जैसे कोई ईमेल एड्रेस या फ़ोन नंबर) का खुलासा होने से रोकने के लिए उसे एक मान में रूपांतरित करते हैं. थर्ड पार्टी प्रायवेसी संरक्षक आइडेंटीफ़ायर की अपने डेटाबेस में आइडेंटीफ़ायरों से तुलना करता है और कोई मैच केवल तभी होगा जब आपने उसी आइडेंटीफ़ायर (जैसे कोई ईमेल एड्रेस) का इस्तेमाल Netflix और थर्ड पार्टी के साथ किया है. अगर कोई मैच होता है, Netflix तब चुन सकता है कि क्या कोई दिया हुआ प्रमोशनल कम्यूनिकेशन आपको थर्ड पार्टी साइट या ऐप पर भेजना है या नहीं, और ऑप्टिमाइज़ कर सकता है और ऑनलाइन एडवर्टाइज़िंग की प्रभाविकता को बेहतर माप सकता है. आप हमारी वेबसाइट के “अकाउंट” सेक्शन के “मार्केटिंग कम्यूनिकेशन” में ऑप्ट आउट कर सकते हैं.
