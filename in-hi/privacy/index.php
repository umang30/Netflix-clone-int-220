<br />
<br />
अगर आप Netflix ऐप का किसी मोबाइल डिवाइस, टैबलेट, या स्ट्रीमिंग मीडिया डिवाइस पर इस्तेमाल करते हैं, तो हम एक रीसेट होने योग्य डिवाइस आइडेंटीफ़ायर आपकी डिवाइस से एकत्रित करते हैं. रीसेट होने योग्य डिवाइस आइडेंटीफ़ायर (जिसे एडवर्टाइज़िंग आइडेंटीफ़ायर के नाम से भी जाना जाता है) कुकीज़ के समान होते हैं और बहुत-से मोबाइल और टैबलेट डिवाइस पर पाए जाते हैं (उदाहरण के लिए, "आइडेंटीफ़ायर फॉर एडवर्टाइज़र" (या IDFA) Apple iOS डिवाइसों पर और द "Google एडवर्टाइज़िंग ID" Android डिवाइसों पर), और कुछ स्ट्रीमिंग मीडिया डिवाइसों पर. कुकीज़ की तरह, रीसेट होने योग्य डिवाइस आइडेंटीफ़ायर का इस्तेमाल ऑनलाइन विज्ञापन को अधिक प्रासंगिक और विश्लेषणात्मक बनाने और ऑप्टिमाइज़ेशन उद्देश्य के लिए किया जाता है.
